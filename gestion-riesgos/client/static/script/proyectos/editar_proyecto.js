
function editar_proyecto() {
    let jp = document.getElementById("jp")
    jp.disabled = false
    let clientes = document.getElementById("cliente")
    clientes.disabled = false
    let trabajadores = document.getElementById("trabajadores")
    trabajadores.disabled = false
    let servicio = document.getElementById("servicio")
    servicio.disabled = false
    let fecha_inicio = document.getElementById("fecha_inicio")
    fecha_inicio.disabled = false
    let fecha_fin = document.getElementById("fecha_fin")
    fecha_fin.disabled = false
    let fecha_fin_prevista = document.getElementById("fecha_fin_prevista")
    fecha_fin_prevista.disabled = false
}
